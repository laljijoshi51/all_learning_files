/*
Understand size concept
*/

#include<stdio.h>

int main(void){

    printf("size char : %d\n",sizeof(char));
    printf("size short int : %d\n",sizeof(short int));
    printf("size int : %d\n",sizeof(int));
    printf("size long int : %d\n",sizeof(long int));
    printf("size long long int : %d\n",sizeof(long long int));
    printf("size float : %d\n",sizeof(float));
    printf("size double : %d\n",sizeof(double));
    printf("size long double : %d\n",sizeof(long double));

}
